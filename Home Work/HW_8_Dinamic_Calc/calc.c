#include "calc.h"

float add(float x, float y) {
    float z = 0;
    z = x + y;
    return z;
}

float minus(float x, float y) {
    float z = 0;
    z = x - y;
    return z;
}

float multiply(float x, float y) {
    float z = 0;
    z = x * y;
    return z;
}

float divide(float x, float y) {
    float z = 0;
    z = x / y;
    return z;
}

